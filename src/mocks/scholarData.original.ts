import type { AuthorRecord, PaperRecord, VenueRecord } from '../types/domain'

// =========================
// TIPOS AUXILIARES
// =========================
type Region = 'Centro-Oeste' | 'Sudeste' | 'Sul'
type PaperNodeType = 'capital' | 'metropole' | 'regional_hub' | 'connector' | 'innovation_hub'
type ImportanceLevel = 'low' | 'medium' | 'high' | 'critical'

type GraphPosition = {
  x: number
  y: number
}

export type EnrichedVenueRecord = VenueRecord & {
  uf: string
  region: Region
  color: string
  description: string
  position: GraphPosition
  tourismWeight: number
  logisticsScore: number
  economicProfile: string
}

export type EnrichedAuthorRecord = AuthorRecord & {
  specialty: string
  regionFocus: Region[]
  profileColor: string
  tags: string[]
  mainHubPaperId: string
  collaborationAuthorIds: string[]
  influenceScore: number
  description: string
}

export type EnrichedPaperRecord = PaperRecord & {
  slug: string
  nodeType: PaperNodeType
  region: Region
  uf: string
  position: GraphPosition
  keywords: string[]
  importance: ImportanceLevel
  visualSize: number
  estimatedTraffic: number
  inboundCount: number
  outboundCount: number
  totalConnections: number
  isHub: boolean
  routeStrength: number
  connectedVenueIds: string[]
  summary: string
}


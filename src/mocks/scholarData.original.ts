const paperById = Object.fromEntries(mockPapers.map((paper) => [paper.paperId, paper]))

function getConnectedVenueIds(paper: PaperRecord): string[] {
  const ids = new Set<string>()

  for (const refId of paper.references) {
    const refPaper = paperById[refId]
    if (refPaper?.venueId && refPaper.venueId !== paper.venueId) {
      ids.add(refPaper.venueId)
    }
  }

  for (const citationId of paper.citations) {
    const citationPaper = paperById[citationId]
    if (citationPaper?.venueId && citationPaper.venueId !== paper.venueId) {
      ids.add(citationPaper.venueId)
    }
  }

  return [...ids]
}

function computeRouteStrength(paper: PaperRecord) {
  return paper.references.length * 10 + paper.citations.length * 12 + Math.floor(paper.citationCount / 100)
}

function getImportance(totalConnections: number, citationCount: number): ImportanceLevel {
  if (citationCount >= 1000 || totalConnections >= 5) return 'critical'
  if (citationCount >= 700 || totalConnections >= 4) return 'high'
  if (citationCount >= 500 || totalConnections >= 3) return 'medium'
  return 'low'
}

